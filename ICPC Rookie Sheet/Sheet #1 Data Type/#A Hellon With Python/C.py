#Solution:
a, b = map(int, input().split())

#print(a, "+", b,"=",(a+b)) | This is an alternative way to print
print(f"{a} + {b} = {a+b}")

#print(a, "*", b,"=",(a*b)) | This make code more readable
print(f"{a} * {b} = {a*b}")

#print(a, "-", b,"=",(a-b)) | This make code more faster
print(f"{a} - {b} = {a-b}")
